class Personas(object):
    nombre=None
    apellido=None
    fecha_nac=None
    dni=None

    def setAgregarNombre(self,n):
        self.nombre=n

    def setAgregarApellido(self,ap):
        self.apellido=ap

    def setAgregarFechaN(self,fecha):
        self.fecha_nac=fecha

    def setAgregarDni(self,d):
        self.dni=d

